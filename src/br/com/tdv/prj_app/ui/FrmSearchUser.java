    public User getUserSelected() {
        String idStr = (String) this.jTable1.getValueAt(this.jTable1.getSelectedRow(), 0);
        int id = Integer.parseInt(idStr);
        this.userSelected = this.dao.getUser(id);        
        return this.userSelected;
    }    
    
    private void populeTable() {       
        this.users = this.dao.getAllUsers();
        DefaultTableModel modelTable = (DefaultTableModel) jTable1.getModel();
        while (modelTable.getRowCount() > 0) {
            modelTable.removeRow(0);
        }        
        for (User u : this.users) {
            modelTable.addRow(new Object[] { String.valueOf(u.getId()),
                                             u.getName(), 
                                             u.getEmail(), 
                                             u.getDateResgistration().getTime(),
                                             u.getPassword() });        
        }
    }

