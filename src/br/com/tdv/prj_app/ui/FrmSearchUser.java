    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        // Excluir
        try {
            User u = this.getUserSelected();
            if (JOptionPane.showConfirmDialog(rootPane, "Deseja Realmente Excluir Usuario: " + u.getName() + " ?", "Excluir", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
                this.dao.delete(u);
                this.populeTable();
                JOptionPane.showMessageDialog(rootPane, "Usuario Excluido com Sucesso");
            }
        } catch (Exception e) {
            if ("-1".equals(e.getMessage()))
                JOptionPane.showMessageDialog(rootPane, "Usuário não selecionado", "Exluir", JOptionPane.WARNING_MESSAGE);
            else
                JOptionPane.showMessageDialog(rootPane, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

