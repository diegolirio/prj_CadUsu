    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        try {
            FrmSearchUser frm = new FrmSearchUser(this, true); 
            frm.setVisible(true);
            if (frm.selectd) {
                this.user = frm.getUserSelected();
                this.PopuleForm(this.user);
                this.txtPassword.setEnabled(false);
                this.txtConfirm.setEnabled(false);                
            } else {
                this.btnNovoActionPerformed(evt);
            }
        } catch (Exception e) {
            if ("-1".equals(e.getMessage())) {
                JOptionPane.showMessageDialog(rootPane, "Usuário não selecionado", "Alteração", JOptionPane.WARNING_MESSAGE);
                this.btnNovoActionPerformed(evt);
            }
            else
                JOptionPane.showMessageDialog(rootPane, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        this.user = new User();
        this.PopuleForm(user);
        this.txtPassword.setEnabled(true);
        this.txtConfirm.setEnabled(true);                        
    }//GEN-LAST:event_btnNovoActionPerformed

    private void PopuleForm(User user) {
        this.txtName.setText(user.getName());
        this.txtEmail.setText(user.getEmail());
        this.txtPassword.setText(user.getPassword());
        this.txtConfirm.setText(user.getPassword());
    }
    
    private User getUserForm() {
        user.setName(this.txtName.getText());
        user.setEmail(this.txtEmail.getText());
        user.setPassword(this.txtPassword.getText());  
        return user;
    }
    

