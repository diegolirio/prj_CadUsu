    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String messageValidation = "";
        if("".equals(txtName.getText()))
            messageValidation += "Digite o Nome";
        if("".equals(txtEmail.getText())) 
            messageValidation += "\nDigite o Email"; 
        if(user.getId() == 0 && "".equals(txtPassword.getText())) 
            messageValidation += "\nDigite a Senha";      
        if(user.getId() == 0 && "".equals(messageValidation) && !txtPassword.getText().equals(txtConfirm.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Confirmação da senha inválida", "Atenção", JOptionPane.WARNING_MESSAGE);
            this.txtPassword.setText("");
            this.txtConfirm.setText("");
            return;
        }             
        if(!"".equals(messageValidation)) {
            JOptionPane.showMessageDialog(rootPane, messageValidation, "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        this.user = this.getUserForm();
        try {
            if (user.getId() > 0) 
                this.dao.update(user);
            else
                this.dao.insert(user);
            JOptionPane.showMessageDialog(rootPane, "Usuario "+(user.getId() > 0 ? "Alterado" : "Cadastrado")+" com sucesso!!!", "Cadastro de usuario", JOptionPane.INFORMATION_MESSAGE);
            this.txtPassword.setEnabled(false);
            this.txtConfirm.setEnabled(false);
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro: " + ex.getMessage(), "Cadastro de usuario", JOptionPane.ERROR_MESSAGE);
        } 
        
    }//GEN-LAST:event_btnSalvarActionPerformed

